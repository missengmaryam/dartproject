void main(List<String> args) {
  hello() {
    print('hello');
  }

  print(hello());

  String? maryam() {
    print("maryam");
  }

  print(maryam());

  pro() {
    print('programmer');
  }

  print(pro());

  String world() {
    return ('hello world');
  }

  print(world());
}
